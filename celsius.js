//Celsius Calculator

// Formel: Fahrenheit = Celsius * 9/5 + 32.

// Temperatur in Celsius
let celsius = 30;

let fahrenheit = (celsius * 9) / 5 + 32;
// 30 Grad * 9 = 270 Grad
//270 Grad / 5 = 54
// 54 + 32 = 86 Fahrenheit

console.log(
  celsius +
    " Grad Celsius ist sehr warm und umgerechnet entspricht es " +
    fahrenheit +
    " Grad Fahrenheit."
);
